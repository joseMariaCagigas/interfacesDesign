    public void creacionFor(JComboBox cmpnt) {
        for (int i = 0; i < 256; i++) {
            cmpnt.addItem(i);
        }
    }

    public void creacionFila4() {

        boton1 = new Button("Muestra");
        boton2 = new JButton();
        boton3 = new JButton();

        fila4 = new JPanel(new FlowLayout(FlowLayout.LEFT));

        boton2.setText("Aceptar");
        boton3.setText("Cancelar");

        boton1.setEnabled(false);
        boton2.addActionListener(this);
        boton3.addActionListener(this);

        fila4.add(boton1);
        fila4.add(boton2);
        fila4.add(boton3);

        this.add(fila4, LEFT_ALIGNMENT);

    }
    
