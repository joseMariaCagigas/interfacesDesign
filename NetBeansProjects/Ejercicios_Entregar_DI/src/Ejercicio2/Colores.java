    public void creacionFila11() {
        fila1 = new JPanel(new FlowLayout(FlowLayout.LEFT));
        comboBox1 = new JComboBox();
 
        label1 = new JLabel("Rojo: ");
        creacionFor(comboBox1);
        fila1.add(label1);
        fila1.add(comboBox1);
        this.add(fila1);
    }

